#include "libft.h"

char	*ft_strcat(char *s1, char const *s2)
{
	char	*str1;

	str1 = s1;
	while (*str1 != '\0')
		str1++;
	while (*s2 != '\0')
		*str1++ = *s2++;
	*str1 = '\0';
	return (s1);
}
