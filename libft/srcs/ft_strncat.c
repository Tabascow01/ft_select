#include "libft.h"

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	char	*str1;

	str1 = s1;
	while (*str1 != '\0')
		str1++;
	while (n > 0 && *s2 != '\0')
	{
		*str1++ = *s2++;
		n--;
	}
	*str1 = '\0';
	return (s1);
}
