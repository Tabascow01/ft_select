#include "libft.h"

void	ft_putnbr(int nb)
{
	char *nbr;

	nbr = "-2147483648";
	if (nb == -2147483648)
		ft_putstr(nbr);
	else
	{
		if (nb < 0 && nb != -2147483648)
		{
			ft_putchar('-');
			nb = nb * -1;
		}
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
			ft_putchar((nb % 10) + '0');
		}
		if (nb >= 0 && nb < 10)
			ft_putchar(nb + '0');
	}
}
